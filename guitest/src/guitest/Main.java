    private String maxima_edad() {
        DefaultTableModel modelo = (DefaultTableModel) tabla1.getModel();
        double maximo = 0;
        if (modelo.getRowCount() > 0) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
                if (Double.parseDouble(modelo.getValueAt(i, 2).toString())
                        > maximo) {
                    maximo = Double.parseDouble(
                            modelo.getValueAt(i, 2).toString());
                }
            }
        }
        return Double.toString(maximo);

    }


