    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int codigo;
        String nombre, sexo, direccion, idioma;
        Date fecha;
        Color c;
        String h1 = "", h2 = "", h3 = "", h4 = "", h5 = "";  
        try {     
            codigo = Integer.parseInt(tf_codigo.getText());
            nombre = tf_nombre.getText();
            if (rb_m.isSelected()) {
                sexo = "M";
            } else {
                sexo = "F";
            } 
            fecha = jd_fecha.getDate();
            c = bt_color.getBackground(); 
            direccion = ta_direccion.getText();
            idioma = cb_idioma.getSelectedItem().toString();
            
            if (cb_football.isSelected()) {
                h1 = "Football";
            }            
            if (cb_tv.isSelected()) {
                h2 = "tv";
            }
            if (cb_musica.isSelected()) {
                h3 = "musica";
            }
            if (cb_correr.isSelected()) {
                h4 = "correr";
            }
            if (cb_bailar.isSelected()) {
                h5 = "bailar";
            }   
            
            
            if (pb_amigos.getValue() < 10) { 
                contactos x = new contactos(
                        codigo, nombre, sexo, fecha, c,
                        direccion, idioma, h1, h2, h3, h4, h5);
                lista.add(x);
                
                 DefaultComboBoxModel dc=
                    (DefaultComboBoxModel) cb_amigos.getModel();
                dc.addElement(x);
                cb_amigos.setModel(dc);
                
                DefaultComboBoxModel dc2=
                    (DefaultComboBoxModel) cb_amigos.getModel();
                dc2.addElement(x);
                cb_amigos_modificar.setModel(dc2);
                
                
                
                pb_amigos.setValue(lista.size());
                pb_amigos.setString(
                   Integer.toString(pb_amigos.getValue() * 100 / 10) + "%"
                );
                JOptionPane.showMessageDialog(this,
                        "Contacto almacenado exitosamente");
                
                tf_codigo.setText("");
                tf_nombre.setText("");
                rb_m.setSelected(true);
                rb_f.setSelected(false);
                jd_fecha.setDate(new Date());
                bt_color.setBackground(Color.red);
                ta_direccion.setText("");
                cb_football.setSelected(false);
                cb_idioma.setSelectedIndex(0);
                
            }else {
                JOptionPane.showMessageDialog(this,
                        "ya no hay espacio disponible");
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                 "Ocurrio un error y no se guardaron los datos");
        }        
    }//GEN-LAST:event_jButton1MouseClicked

    
    
    

