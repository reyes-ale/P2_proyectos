    private void bt_exportarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_exportarMouseClicked
        DefaultTableModel tabla = (DefaultTableModel) jt_detalles.getModel();
        JFileChooser fc = new JFileChooser();
        FileFilter filter = new FileNameExtensionFilter("Archivos de texto", "txt");
        fc.setFileFilter(filter);
        File archivo = null;
        FileWriter fw = null;
        BufferedWriter bw = null;
        
        if (tabla.getRowCount()>0){
            String temp = "";
        
            for (int j = 0; j < 6; j++) {
                if (j==5){
                    temp+=tabla.getValueAt(0, j);
                    temp+="\n";
                }
                else{
                    temp+=tabla.getValueAt(0, j);
                temp+=";";
                }
            }
            
        int sel = fc.showOpenDialog(this);
        if (sel == JFileChooser.APPROVE_OPTION) {
        //agarrar info de la row en la tabla y escribir eso en el archivo
         archivo = fc.getSelectedFile();
           try {
               fw = new FileWriter(archivo,true);
                bw = new BufferedWriter(fw);
                
                bw.write(temp);
                
                bw.flush();
                bw.close();
                fw.close();
                
           } catch (IOException ex) {
               ex.printStackTrace();
           }
        
        
        }
        JOptionPane.showMessageDialog(this, "Escritura realizada exitosamente");
        
        }
        else {
            JOptionPane.showMessageDialog(this, "La tabla esta vacia");
        }
        
        
    }//GEN-LAST:event_bt_exportarMouseClicked

    private void jmi_agregarvehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmi_agregarvehiculoActionPerformed
        Random r = new Random();
        
        
        if (tf_anio.getText().isEmpty() || tf_marca.getText().isEmpty() || tf_modelo.getText().isEmpty() || tf_precio.getText().isEmpty() || dc_fecha==null
                || (tf_anio.getText().isEmpty() && tf_marca.getText().isEmpty() && tf_modelo.getText().isEmpty() && tf_precio.getText().isEmpty() && dc_fecha==null)){
            try {
                throw new Exception("Asegurese de llenar todos los campos");
            } catch (Exception ex) {
               ex.printStackTrace();
            }
        }
        else {
            
            int anio = Integer.parseInt(tf_anio.getText());
            int codigo = r.nextInt(2000)+1000;
            double precio = Double.parseDouble(tf_precio.getText());
            jl_codigo.setText(Integer.toString(codigo));
            int cont = 0;  
            
            Vehiculo v = new Vehiculo(anio, codigo, tf_marca.getText(), tf_modelo.getText(), dc_fecha.getDate(), precio);
            
            DefaultTreeModel arbol = (DefaultTreeModel) arbol_vehiculos.getModel();
            DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) arbol.getRoot();
            String anioo = tf_anio.getText();
            
                     
            for (int i = 0; i < raiz.getChildCount(); i++) {
                if (raiz.getChildAt(i).toString().equals(anioo)) {
                  DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(v);
                  ((DefaultMutableTreeNode) raiz.getChildAt(i)).add(nodo);
                  cont++;
                }
            }
            
            if (cont == 0) {
                DefaultMutableTreeNode anionuevo = new DefaultMutableTreeNode(anioo);
                DefaultMutableTreeNode nodito = new DefaultMutableTreeNode(v);
                raiz.add(anionuevo);                
                anionuevo.add(nodito);
            }
          arbol.reload();
            
        }
        
    }//GEN-LAST:event_jmi_agregarvehiculoActionPerformed

    private void arbol_vehiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_arbol_vehiculosMouseClicked
        if (evt.isMetaDown()){
            int fila = arbol_vehiculos.getClosestRowForLocation(evt.getX(), evt.getY());
            arbol_vehiculos.setSelectionRow(fila);
            Object temp = arbol_vehiculos.getSelectionPath().getLastPathComponent();
            seleccion = (DefaultMutableTreeNode) temp;
            if (seleccion.getUserObject() instanceof Vehiculo) {
                popup_arbol.show(evt.getComponent(),evt.getX(), evt.getY());
                carrito = (Vehiculo) seleccion.getUserObject();
            }
        }
    }//GEN-LAST:event_arbol_vehiculosMouseClicked

